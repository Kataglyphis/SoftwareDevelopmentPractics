package edu.kit.valaris.generation;

public enum GeneratorSettings {

    // Debug setting
    DEBUG,
    // Place Checkpoint at the entry of the tunnel
    CHECKPOINT_ENTRY,
    // Place Checkpoint at the exit of the tunnel
    CHECKPOINT_EXIT,
    // Place door at the entry of the tunnel
    DOOR_ENTRY,
    // Place door at the exit of the tunnel
    DOOR_EXIT,
    // Generate the starting room
    STARTROOM,
    // Generate the last room
    ENDROOM

}
